package model;

public class TipoEspacoModel {
	private Long idTipoEspaco;
	private String tipoEspaco;
	
	public Long getIdTipoEspaco() {
		return idTipoEspaco;
	}
	
	public void setIdTipoEspaco(Long idTipoEspaco) {
		this.idTipoEspaco = idTipoEspaco;
	}
	
	public String getTipoEspaco() {
		return tipoEspaco;
	}
	
	public void setTipoEspaco(String tipoEspaco) {
		this.tipoEspaco = tipoEspaco;
	}
	
}
